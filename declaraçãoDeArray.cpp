#include <stdio.h>  
int main(){  
    // Declarando um array de inteiros com 5 elementos  
    int array[5] = {10,20,30,40,50};  
    // Acessando e imprimindo os elementos do array
    printf("Elemento 1: \%d\n", array[0]); // Segundo elemento (indice 0)    
    printf("Elemento 2: \%d\n", array[1]); // Segundo elemento (indice 1)  
    printf("Elemento 2: \%d\n", array[2]); // Segundo elemento (indice 2)  
    printf("Elemento 4: \%d \n", array[3]); // Quarto elemento (indice 3)  ;
    printf("Elemento 5: \%d \n", array[4]); // Quinto elemento (indice 4)  ;
    printf("Elemento 6: \%d \n", array[5]); // Quinto elemento (indice 5)  ;
    return 0 ;  
}